//----------------------------------------------------------------------------
// Imports Section
//----------------------------------------------------------------------------
import { Component }                    from '@angular/core';
import { OnInit }                       from '@angular/core';
import { OnDestroy }                    from '@angular/core';
import { Router }                       from '@angular/router';

//----------------------------------------------------------------------------
// Component Configuration Section
//----------------------------------------------------------------------------
@Component({
    selector: 'navbar',
    templateUrl: 'navbar.component.html',
    styleUrls: ['navbar.component.css'],
})
//----------------------------------------------------------------------------
// Component Class Section
//----------------------------------------------------------------------------
export class NavbarComponent implements OnInit, OnDestroy
{
    //------------------------------------------------------------------------
    // Public Fields Section
    //------------------------------------------------------------------------


    //------------------------------------------------------------------------
    // Private Fields Section
    //------------------------------------------------------------------------
    private router                          : Router;


    //------------------------------------------------------------------------
    // Constructor Method Section
    //------------------------------------------------------------------------
    constructor(rt: Router)
    {
        this.router = rt;
    }


    //------------------------------------------------------------------------
    // Component Lifecycle Eventhandler Methods Section
    //------------------------------------------------------------------------
    ngOnInit()
    {

    }
    //------------------------------------------------------------------------
    ngOnDestroy()
    {

    }

    //------------------------------------------------------------------------
    // Eventhandler Methods Section
    //------------------------------------------------------------------------
    txtInput_keyUpEnter(userId: string)
    {
        this.navigate(userId);
    }
    //------------------------------------------------------------------------
    cmdSearch_click(userId: string)
    {
        this.navigate(userId);
    }


    //------------------------------------------------------------------------
    // Private Methods Section
    //------------------------------------------------------------------------
    private navigate(userId: string)
    {
        if (!userId)
        {
            return;
        }

        this.router.navigate(['/usuario', userId]);
    }


}
